#!/usr/bin/env bash
set -ev

echo "$DOCKER_PASSWORD" | docker login -u "$DOCKER_USERNAME" --password-stdin

docker tag ${PROJECT}:${TRAVIS_COMMIT} ${CONTAINER_REGISTRY_HOST}/${PROJECT}:${TRAVIS_COMMIT}
docker push ${CONTAINER_REGISTRY_HOST}/${PROJECT}:${TRAVIS_COMMIT}
docker tag ${PROJECT}:${TRAVIS_COMMIT} ${CONTAINER_REGISTRY_HOST}/${PROJECT}:${TRAVIS_BRANCH}
docker push ${CONTAINER_REGISTRY_HOST}/${PROJECT}:${TRAVIS_BRANCH}
